        /// <summary>
        /// Actually apply the damage to health right now.
        /// </summary>
        public virtual void ApplyDamage(int damage)
        {
            int hitPoints = HP;
            
            hitPoints -= damage;
            
            if (hitPoints < 0)
            {
                hitPoints = 0;
            }

            if (hitPoints > MaxHealth)
            {
                hitPoints = MaxHealth;
            }
            
            HP = hitPoints;
        }
        
        /// <summary>
        /// Actually apply reducing the arcana by the specified cost right now.
        /// </summary>
        public virtual void ReduceArcana(int spellCost)
        {
            int arc = Arcana;
            
            arc -= spellCost;
            
            if (arc < 0)
            {
                arc = 0;
            }

            if (arc > MaxArcana)
            {
                arc = MaxArcana;
            }
            
            Arcana = arc;
        }

        /// <summary>
        /// Actually apply change to the resolve right now.
        /// </summary>
        public virtual void ApplyResolve(int resolveChange)
        {
            int res = Resolve;
            
            res += resolveChange;
            
            if (res < 0)
            {
                res = 0;
            }

            if (res > MaxResolve)
            {
                res = MaxResolve;
            }
            
            Resolve = res;
        }
        
        /// <summary>
        /// Actually apply change to the resolve right now.
        /// </summary>
        public virtual void ApplyWisdom(int wisdomChange)
        {
            int wis = Wisdom;
            
            wis += wisdomChange;
            
            if (wis < 0)
            {
                wis = 0;
            }

            if (wis > MaxWisdom)
            {
                wis = MaxWisdom;
            }
            
            Wisdom = wis;
        }
        
